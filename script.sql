CREATE PROCEDURE [dbo].[EnrollStudent164]
    @StudentID INT,
    @CourseID INT
AS
BEGIN
    BEGIN TRY
        IF EXISTS (SELECT 1 FROM ENROLLMENT164 
                   WHERE StudentID = @StudentID AND CourseID = @CourseID)
            RAISERROR('DUPLICATE_ENROLLMENT', 16, 1);
            
        INSERT INTO ENROLLMENT164 (StudentID, CourseID)
        VALUES (@StudentID, @CourseID);
        
        PRINT 'Enrollment successful!';
    END TRY
    BEGIN CATCH
        DECLARE @ErrorMessage NVARCHAR(4000) = ERROR_MESSAGE();
        IF @ErrorMessage = 'DUPLICATE_ENROLLMENT'
            RAISERROR('Student already enrolled in this course', 16, 1);
        ELSE
            RAISERROR('Enrollment failed', 16, 1);
    END CATCH
END;


