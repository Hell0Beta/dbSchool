CREATE FUNCTION [dbo].[CalculateGPA164](@StudentID INT)
RETURNS DECIMAL(3,2)
AS
BEGIN
    RETURN (
        SELECT AVG(CASE Grade
            WHEN 'A' THEN 4.0
            WHEN 'B' THEN 3.0
            WHEN 'C' THEN 2.0
            ELSE 0.0
        END)
        FROM GRADE164
        WHERE StudentID = @StudentID
    )
END;

