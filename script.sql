CREATE PROCEDURE [dbo].[CheckPermission164]
    @Username VARCHAR(255),
    @PermissionName VARCHAR(255)
AS
BEGIN
    SELECT CASE WHEN EXISTS (
        SELECT 1
        FROM USER164 u
        JOIN ROLE_PERMISSION164 rp ON u.RoleID = rp.RoleID
        JOIN PERMISSION164 p ON rp.PermissionID = p.PermissionID
        WHERE u.Username = @Username
        AND p.PermissionName = @PermissionName
    ) THEN 1 ELSE 0 END AS HasPermission
END;


